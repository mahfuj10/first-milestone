var color = 'red';

switch (color) {
    case 'blue':
        console.log('iam a blue');
        break;
    case 'yellow':
        console.log('iam a yellow');
        break;
    case 'red':
        console.log('iam a red');
        break;

    default:
        console.log('this is a black');
        break;
}